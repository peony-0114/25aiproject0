# 직업 추천 함수
def get_movie_recommendation(mbti_type):
    movie_info = mbti_movies.get(mbti_type)
    if movie_info:
        return movie_info["movie"], movie_info["reason"]
    return "🌟 MBTI를 다시 선택해주세요!", ""

